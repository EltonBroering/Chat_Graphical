    private void jBConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConectarActionPerformed

        if (jBConectar.getText().equals("Conectar")) {
            // fazendo cliente conectar na localhost
            this.cliente = new ClienteThread(this, this.jTFPessoa.getText(), this.jTFIP.getText(), Integer.parseInt(this.jTFPorta.getText()));
            //disparando a thread
            this.cliente.start();
            jBConectar.setText("Desconectar");
        } else {
            // envia o codigo secreto para sair: "SairDaSala"
            this.cliente.enviaMensagem("SairDaSala");
            jBConectar.setText("Conectar");
        }
        this.jTFTexto.requestFocus();
    }//GEN-LAST:event_jBConectarActionPerformed

