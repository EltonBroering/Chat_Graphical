    private void jTFTextoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTFTextoActionPerformed

        // imprimindo a mensagem na janela de bate papo
        jTAChat.append("eu: " + jTFTexto.getText() + "\n");

        // enviando a mensagem pela conexao de rede
        this.cliente.enviaMensagem(jTFTexto.getText());

        // limpando a caixa de texto
        jTFTexto.setText("");
        jTFTexto.requestFocus();
    }//GEN-LAST:event_jTFTextoActionPerformed

    private void jTBIniciarServidorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTBIniciarServidorActionPerformed

        if (jTBIniciarServidor.isSelected()) {
            // iniciando servidor
            this.hospedeiro = new ServidorDaemon(Integer.parseInt(jTFPorta.getText()));
            this.hospedeiro.start();
            System.out.println("Servidor iniciado");
            try {
                Thread.sleep(100);
            } catch (Exception ex) {
            }
            // aguardando um tempo antes de tentar conectar
            this.jBConectarActionPerformed(evt);
        } else {
            this.hospedeiro.parar();
            try {
                Thread.sleep(100);
            } catch (Exception ex) {
            }
            this.jBConectarActionPerformed(evt);
        }


    }//GEN-LAST:event_jTBIniciarServidorActionPerformed

